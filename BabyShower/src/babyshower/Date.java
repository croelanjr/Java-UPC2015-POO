
package babyshower;


public class Date {
    private int dia;
    private int mes;
    private int anho;

    public Date() {
        this.dia = 26;
        this.mes = 3;
        this.anho = 2012;
    }

    public int getAnho() {
        return anho;
    }

    public int getDia() {
        return dia;
    }

    public int getMes() {
        return mes;
    }
    
    
}
